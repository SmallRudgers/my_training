my_string = input("Введите что вам угодно: ")

print("Кол-во символов в введенной строке:", len(my_string))

print("Перевод в верхний регистр: ", my_string.upper())

print("Перевод в нижний регистр: ", my_string.lower())

print("Без пробелов: ", my_string.replace( ' ', ''))

print("Первый символ: ",my_string[0])

print("Последний символ: ", my_string[-1])
